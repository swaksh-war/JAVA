public class IfElse {
    public static void main(String[] args){
        if(20>14){
            System.out.println("20 is greater than 14");
        }

        int time=18;
        if(time<18){
            System.out.println("Its still Daytime");
        } else if(time == 18){
            System.out.println("Its just twilight");
        } else{
            System.out.println("Its Night Time");
        }
    }
    
}
