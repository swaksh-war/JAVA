package Learning;
public class Variable {
    public static void main(String[] args){
        String name="Swaksh";
        System.out.println(name);
        
        int myNum=69;
        System.out.print(myNum);

        float myFloat=5.69f;
        System.out.println(myFloat);

        int myInt=12;
        myInt=13;
        System.out.println(myInt);

        boolean myBool=true;
        System.out.println(myBool);
        
        final int myFinalint=15;
        System.out.println(myFinalint);//once anything is declared using final keyword its value cant be changed
        


    }
}
